    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        
        n1=Double.valueOf(txtp1.getText());
        n2=Double.valueOf(txtp2.getText());
        n3=Double.valueOf(txtp3.getText());
        promedio= (n1+n2+n3)/3;
        resultado.setText(String.valueOf(promedio));
        if(promedio <11){
           this.rendimiento = "Desaprobado";
       } else{
           this.rendimiento = "Aprobado";
       }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        //CODIGO DEL BOTON AGREGAR.. CREAMOS UNA ARREGLO
        String []agregar=new String[5];
        agregar[0]=txtcodigo.getText();
        agregar[1]=txtnombre.getText();
        agregar[2]=txtcurso.getText();
        agregar[3]=resultado.getText();
        agregar[4]=rendimiento;
        
        model.addRow(agregar);
        
    }//GEN-LAST:event_btnagregarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int Fila=tabladedatos.getSelectedRow();
       if(Fila>=0){
           model.removeRow(Fila);
       }else{
           JOptionPane.showMessageDialog(null, "No hay datos que eliminar");
       }
    }//GEN-LAST:event_btneliminarActionPerformed

